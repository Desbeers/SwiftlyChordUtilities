import Foundation
import CoreGraphics

struct LineConfig {
    /// spacing between lines
    let spacing: CGFloat
    /// starting point of line
    let margin: CGFloat
    /// the length of the line
    let length: CGFloat
    /// Number of lines to draw
    let count: Int
}
